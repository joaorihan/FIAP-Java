    private void acessarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acessarButtonActionPerformed
        String login = loginTextField.getText();
        String password = passwordTextField.getText();
        

        if(login.equals("admin") && password.equals("admin")){
            loginRealizado.setVisible(true);
            JOptionPane.showMessageDialog(null, "Seja bem vindo");
            new Consultar_GUI().setVisible(true);
            this.dispose();
        } else{
            tentativas -= 1;
            
            if (tentativas == 0){
                JOptionPane.showMessageDialog(null, "Você excedeu o numero máximo de erros. Bloqueando");
                System.exit(0);
            }
            
            JOptionPane.showMessageDialog(null, "Senha ou login inválidos. \nVocê tem " + tentativas + " tentativas restantes");
        }
    }//GEN-LAST:event_acessarButtonActionPerformed

