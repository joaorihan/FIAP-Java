    private void readButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_readButtonActionPerformed
        if (arbitro == null){
            JOptionPane.showMessageDialog(null, "Não há nenhum árbitro cadastrado ainda!");
            return;
        }
        
        JOptionPane.showMessageDialog(null, "Árbitro Cadastrado\nCPF: " + arbitro.cpf + " \nNome: " + arbitro.nome + "\nIdade: " + arbitro.idade + "\nContato: " + arbitro.contato + "\nSerie: " + arbitro.serie + "\nDisponivel: " + arbitro.disponivel);
    }//GEN-LAST:event_readButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        if (arbitro == null){
            JOptionPane.showMessageDialog(null, "Não há nenhum árbitro cadastrado ainda!");
            return;
        }
        
        arbitro = null;
        JOptionPane.showMessageDialog(null, "Árbitro deletado com sucesso!");
        
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        if (arbitro == null){
            JOptionPane.showMessageDialog(null, "Não há nenhum árbitro cadastrado ainda!");
            return;
        }
        
        try {
            arbitro = new Arbitro_DAO(cpf.getText(), nome.getText(), Integer.parseInt(idade.getText()), contato.getText(), (String) serie.getSelectedItem(), (String) disponivel.getSelectedItem());    
        }
        catch (NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Sua idade só pode conter números!");
            return;
        }
        JOptionPane.showMessageDialog(null, "Árbitro atualizado com sucesso!");
    }//GEN-LAST:event_updateButtonActionPerformed

