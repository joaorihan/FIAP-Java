    private void createButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createButtonActionPerformed
        try {
            arbitro = new Arbitro_DAO(cpf.getText(), nome.getText(), Integer.parseInt(idade.getText()), contato.getText(), (String) serie.getSelectedItem(), (String) disponivel.getSelectedItem());    
        }
        catch (NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Sua idade só pode conter números!");
            return;
        }
        JOptionPane.showMessageDialog(null, "Árbitro cadastrado com sucesso!");
    }//GEN-LAST:event_createButtonActionPerformed

